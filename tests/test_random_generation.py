import numpy as np
from python_anesthesia_simulator import patient

# %% Initialization patient
ts = 5
age, height, weight, gender = 74, 164, 88, 1
np.random.seed(42)
george_nominal = patient.Patient(
    [age, height, weight, gender],
    ts=ts,
)
george_random = patient.Patient(
    [age, height, weight, gender],
    ts=ts,
    random_PK=True,
    random_PD=True,
)

george_truncated = patient.Patient(
    [age, height, weight, gender],
    ts=ts,
    random_PK=True,
    random_PD=True,
    truncated=1,
)

# %% run a simple simulation
N_simu = int(60 * 60/ts)

uP, uR, uN = 0.15, 0.1, 2


for index in range(N_simu):
    george_nominal.one_step(
        u_propo=uP,
        u_remi=uR,
        u_nore=uN,
    )
    george_random.one_step(
        u_propo=uP,
        u_remi=uR,
        u_nore=uN,
    )
    george_truncated.one_step(
        u_propo=uP,
        u_remi=uR,
        u_nore=uN,
    )


# %% Test function

def test_different_output():
    """Ensure that the random patient have different output than the nominal"""
    assert not np.allclose(george_nominal.dataframe['BIS'], george_random.dataframe['BIS'])
    assert not np.allclose(george_nominal.dataframe['MAP'], george_random.dataframe['MAP'])
    assert not np.allclose(george_nominal.dataframe['CO'], george_random.dataframe['CO'])
    assert not np.allclose(george_nominal.dataframe['TOL'], george_random.dataframe['TOL'])

    assert not np.allclose(george_nominal.dataframe['BIS'], george_truncated.dataframe['BIS'])
    assert not np.allclose(george_nominal.dataframe['MAP'], george_truncated.dataframe['MAP'])
    assert not np.allclose(george_nominal.dataframe['CO'], george_truncated.dataframe['CO'])
    assert not np.allclose(george_nominal.dataframe['TOL'], george_truncated.dataframe['TOL'])


def test_acceptable_random_output():
    """Ensure the physical feasibility of the output generated by the random patient."""
    assert np.all(george_random.dataframe['BIS'] <= 100)
    assert np.all(george_random.dataframe['BIS'] >= 0)
    assert np.all(george_random.dataframe['TOL'] <= 1)
    assert np.all(george_random.dataframe['TOL'] >= 0)
    assert np.all(george_random.dataframe['MAP'] <= 200)
    assert np.all(george_random.dataframe['MAP'] >= 0)
    assert np.all(george_random.dataframe['CO'] <= 20)
    assert np.all(george_random.dataframe['CO'] >= 0)


def test_truncated_random_output():
    """Ensure the physical feasibility of the output generated by the truncated random patient."""
    assert np.all(george_truncated.dataframe['BIS'] <= 100)
    assert np.all(george_truncated.dataframe['BIS'] >= 0)
    assert np.all(george_truncated.dataframe['TOL'] <= 1)
    assert np.all(george_truncated.dataframe['TOL'] >= 0)
    assert np.all(george_truncated.dataframe['MAP'] <= 200)
    assert np.all(george_truncated.dataframe['MAP'] >= 0)
    assert np.all(george_truncated.dataframe['CO'] <= 20)
    assert np.all(george_truncated.dataframe['CO'] >= 0)


if __name__ == '__main__':
    test_different_output()
    test_acceptable_random_output()
    test_truncated_random_output()

    print("All tests passed successfully.")
